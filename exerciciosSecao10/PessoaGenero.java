public class PessoaGenero {

    private double altura;
    private String genero;

    PessoaGenero(double altura, String genero) {
        this.altura = altura;
        this.genero = genero;
    }

    public double getAltura() {
        return this.altura;
    }
    public String getGenero() {
        return this.genero;
    }
}
